    void radiobutton() {
        if (rb2D.isSelected()) {
            tipo = 0;
        } else if (rb3D.isSelected()) {
            tipo = 1;
        } else {
            tipo = 2;
        }
        if (rbTodos.isSelected()) {
            clasificacion = 0;
        } else if (rbMayoresde15.isSelected()) {
            clasificacion = 1;
        } else if (rbMayoresde18.isSelected()) {
            clasificacion = 2;
        } else {
            clasificacion  = 3;
        }
    }

