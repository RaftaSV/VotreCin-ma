    void lectura() {
        if (edicion == 0) {
            panelCRUD.setVisible(false);
            btnLectura.setText("Lectura");
            edicion = 1;
        } else {
            btnLectura.setText("Edicion");
            panelCRUD.setVisible(true);
            edicion = 0;
        }
        limpiar();
    }
