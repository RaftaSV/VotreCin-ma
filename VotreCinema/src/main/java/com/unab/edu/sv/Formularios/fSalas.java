    public void ajustartabla() {
        // Ocultar la columna del ID de la pelicula
        tbSalas.getColumnModel().getColumn(0).setMaxWidth(0);
        tbSalas.getColumnModel().getColumn(0).setMinWidth(0);
        tbSalas.getTableHeader().getColumnModel().getColumn(0).setMaxWidth(0);
        tbSalas.getTableHeader().getColumnModel().getColumn(0).setMinWidth(0);

        //Centrar en texto del encabezado de la tabla
        DefaultTableCellRenderer render = (DefaultTableCellRenderer) tbSalas.getTableHeader().getDefaultRenderer();
        render.setHorizontalAlignment(JLabel.CENTER);
        DefaultTableCellRenderer al = new DefaultTableCellRenderer();
        al.setHorizontalAlignment(SwingConstants.CENTER);
        tbSalas.getColumnModel().getColumn(0).setCellRenderer(al);
        tbSalas.getColumnModel().getColumn(1).setCellRenderer(al);
        tbSalas.getColumnModel().getColumn(2).setCellRenderer(al);

    }

    void CargarTabla() {
        String TITULOS[] = {"ID", "CAPACIDAD", "NUMERO DE SALA"};
        ClsSalas sala = new ClsSalas();
        DefaultTableModel model = new DefaultTableModel(null, TITULOS);
        ArrayList<Salas> sal = sala.MostrarSalas();
        String filas[] = new String[4];
        for (var i : sal) {
            filas[0] = String.valueOf(i.idSala);
            filas[1] = String.valueOf(i.getCapacidad());
            filas[2] = String.valueOf(i.getNumero_Sala());
            model.addRow(filas);
        }
        tbSalas.setModel(model);

    }

    public void limpiar() {
        txtCapacidad.setText("");
        txtNumerodeSala.setText("");
        estado = 0;
        id = 0;

    }

    int estado = 0;
    int id = 0;

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (estado == 0) {

            if (txtCapacidad.getText() == "" || txtCapacidad.getText().isEmpty() || txtCapacidad.getText() == null) {
                JOptionPane.showMessageDialog(null, "Es necesario agregar Capacidad");
                txtCapacidad.requestFocus();
            } else if (txtNumerodeSala.getText() == "" || txtNumerodeSala.getText().isEmpty() || txtNumerodeSala.getText() == null) {
                JOptionPane.showMessageDialog(null, "Es necesario agregar Numero de Sala");
                txtNumerodeSala.requestFocus();
            } else {
                Salas sa = new Salas();
                sa.setCapacidad(Integer.valueOf(txtCapacidad.getText()));
                sa.setNumero_Sala(Integer.valueOf(txtNumerodeSala.getText()));
                ClsSalas sala = new ClsSalas();
                sala.GuardarSala(sa);
                limpiar();
                CargarTabla();
                ajustartabla();
            }
        } else if (estado == 1) {

            if (txtCapacidad.getText() == "" || txtCapacidad.getText().isEmpty() || txtCapacidad.getText() == null) {
                JOptionPane.showMessageDialog(null, "Es necesario agregar Capacidad");
                txtCapacidad.requestFocus();
            } else if (txtNumerodeSala.getText() == "" || txtNumerodeSala.getText().isEmpty() || txtNumerodeSala.getText() == null) {
                JOptionPane.showMessageDialog(null, "Es necesario agregar Numero de Sala");
                txtNumerodeSala.requestFocus();
            } else {
                Salas sal = new Salas();
                sal.setIdSala(id);
                sal.setCapacidad(Integer.valueOf(txtCapacidad.getText()));
                sal.setNumero_Sala(Integer.valueOf(txtNumerodeSala.getText()));
                ClsSalas salas = new ClsSalas();
                salas.ActualizarSala(sal);

                CargarTabla();
                ajustartabla();
            }

        }
    }//GEN-LAST:event_btnGuardarActionPerformed

